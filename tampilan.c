void bingkai(int p, int l, int x, int y, int c)
{
    p-=1;
    l-=1;
    textcolor(c);
    for (int a = 0; a <= p; a++)
    {
        alya(x+a, y);
        printf("%c", 219);
        alya(x + a, y + l);
        printf("%c", 219);
    }

    for (int b = 0; b < l; b++)
    {
        alya(x, y + b);
        printf("%c", 219);
        alya(p + x, y + b);
        printf("%c", 219);
    }
}
